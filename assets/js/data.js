const STORAGE_KEY = "collier-site-data";
const LANG_KEY = "collier-lang";

function getSiteData() {
  const raw = localStorage.getItem(STORAGE_KEY);
  if (!raw) return structuredClone(DEFAULT_DATA);
  try {
    const parsed = JSON.parse(raw);
    const importedAvailable = typeof WORDPRESS_PRODUCTS !== "undefined" && Array.isArray(WORDPRESS_PRODUCTS);
    const hasImportedIds =
      Array.isArray(parsed.products) && parsed.products.some((product) => Number(product?.id) >= 100);
    if (importedAvailable && !hasImportedIds) {
      parsed.products = structuredClone(WORDPRESS_PRODUCTS);
      saveSiteData(parsed);
    }
    return parsed;
  } catch (_) {
    return structuredClone(DEFAULT_DATA);
  }
}

function saveSiteData(data) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(data));
}

function getLang() {
  return localStorage.getItem(LANG_KEY) || "he";
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
}

function formatPrice(value, lang) {
  const locale = lang === "he" ? "he-IL" : "en-US";
  return new Intl.NumberFormat(locale, {
    style: "currency",
    currency: "ILS",
    maximumFractionDigits: 0
  }).format(value);
}

